/*
    El "DOM" (Modelo de Objeto del Documento), también conocido como árbol de elementos, 
    es una representación en forma de árbol de la estructura de un documento HTML. 
    Cada nodo en el árbol de elementos representa un elemento HTML en la página web. 
    El árbol de elementos comienza con el elemento raíz, que es "document", y luego se descompone 
    en elementos hijo, nietos, bisnietos, etc.
    Cada elemento tiene propiedades, como id, class, name, value, placeholder etc... 
    que pueden ser accedidas y manipuladas con JavaScript.
*/