# los operadores aritméticos se utilizan para realizar operaciones matemáticas en los datos numéricos.
# Suma (+): suma dos valores.
a = 5
b = 3
c = a + b   # Equivale a 8


# Resta (-): resta dos valores.
a = 5
b = 3
c = a - b  # Equivale a 2


# Multiplicación (*): multiplica dos valores.
a = 5
b = 3
c = a * b  # Equivale a 15


# División (/): divide el valor de la izquierda por el valor de la derecha.
# Si ambos operandos son enteros, la división es una división entera y devuelve un resultado entero.
# Si al menos uno de los operandos es un número de punto flotante, la división es una división en
# coma flotante y devuelve un resultado en coma flotante.
a = 10
b = 3
c = a / b  # Equivale a 3.3333333333333335


# Módulo (%): devuelve el resto de la división de la izquierda por la derecha.
a = 10
b = 3
c = a % b  # Equivale a 1


# Potencia (**): eleva el valor de la izquierda a la potencia del valor de la derecha.
a = 2
b = 3
c = a ** b  # Equivale a 8


# División entera (//): divide el valor de la izquierda por el valor de la derecha, pero el resultado es entero.
# Nota: Si el resultado es decimal sera redondeado.
a = 10
b = 3
c = a // b  # Equivale a 3
