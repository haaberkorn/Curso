# Los operadores comparativos se utilizan para comparar dos valores
# y producir un resultado booleano (True o False).


# Variables a comparar
variable1 = 5
variable2 = 30


# >: mayor que
print(variable1 > variable2)

# <: menor que
print(variable1 < variable2)

# >=: mayor o igual que
print(variable1 >= variable2)
print(variable2 >= variable1)

# <=: menor o igual que
print(variable1 <= variable2)
print(variable2 <= variable1)

# ==: igual que
print(variable1 == variable2)

# !=: distinto de
print(variable1 != variable2)
