"""
los operadores de asignación se utilizan para asignar valores a variables. 
A diferencia de otros lenguajes de programación, Python permite la asignación 
de múltiples variables en una sola línea.
"""


# Operador de asignacion: =
miVariable = "Hola"

# Tambien podemos asignar multiples variables al mismo tiempo.
variable1, variable2, variable3 = "Hola", 2, [True, False, True]


# Operadores especiales

# Operador de asignacion y suma: +=
miVariable2 = 10
miVariable2 += 5  # Ahora esto vale 15


# Operador de asignacion y resta: -=
miVariable3 = 10
miVariable3 -= 5  # Ahora esto vale 5


# Operador de asignacion y multiplicacion: *=
miVariable4 = 10
miVariable4 *= 5  # Ahora esto vale 50


# Operador de asignacion y division: /=
miVariable5 = 10
miVariable5 /= 5  # Ahora esto vale 2


# Operador de asignacion y modulo: %=
miVariable6 = 10
miVariable6 %= 5  # Ahora esto vale 2


# Operador de asignacion y potanciacion: **=
miVariable7 = 2
miVariable7 **= 3   # Ahora esto vale 8 (2 elevado a la 3:  2 x 2 x 2 = 8)
